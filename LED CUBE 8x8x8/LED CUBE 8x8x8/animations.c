void anim4(void)
{
	const uint8_t anim_frames=51, frame_in_anim_repeats=18, repeats_of_anim=1;
	
	position++;
	if (position>=9)
	{
		layer++;
		if (layer>=8)
		{
			layer=0;
			frame_repeats++;
			if (frame_repeats>=frame_in_anim_repeats)
			{
				frame_repeats=0;
				frame++;
				if (frame==anim_frames)
				{
					frame=0;
					anim_repeats++;
					if (anim_repeats==repeats_of_anim)
					{
						anim_repeats=0;
						anim++;
						anim=anim%COUNT_OF_ANIMS;
					}
				}
			}
		}
	}
	if (position<9)
	{
		SPI_set_SS_line(1);
		SPDR=animFOUR[frame][layer][position];
	}else
		position=-1;
		/* value'-1' is to recompense incrementation before sending data to the shift register*/
}


